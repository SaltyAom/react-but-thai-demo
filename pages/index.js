import { ใช้สถานะ, ชิ้นส่วน } from 'react-but-thai'

const แอป = () => {
    let [เคาน์เตอร์, เซ็ตเคาน์เตอร์] = ใช้สถานะ(0)

    let เพิ่มเคาน์เตอร์ทีละหนึ่ง = () => {
        เซ็ตเคาน์เตอร์(เคาน์เตอร์ + 1)
    }

    return (
        <ชิ้นส่วน>
            <h1>ตอบสนองแต่ไทย</h1>
            <p>เคาน์เตอร์</p>
            <button onClick={เพิ่มเคาน์เตอร์ทีละหนึ่ง}>
                {เคาน์เตอร์}
            </button>
        </ชิ้นส่วน>
    )
}

export default แอป
